#include <stdio.h>

int main(void)
{
	int arr1[5] = { 1,2,3,4,5 };  // 순차적으로 초기화
	int arr2[] = { 1,2,3,4,5,6,7 };  // 자동으로 길이정보 삽입
	int arr3[5] = { 1,2 };  // 비어있는 배열요소는 0으로
	int ar1Len, ar2Len, ar3Len, i;  // int형 변수 선언

	printf("배열 arr1의 크기: %d \n", sizeof(arr1));
	printf("배열 arr2의 크기: %d \n", sizeof(arr2));
	printf("배열 arr3의 크기: %d \n", sizeof(arr3));

	ar1Len = sizeof(arr1) / sizeof(int);  // 배열 arr1의 길이 계산
	ar2Len = sizeof(arr2) / sizeof(int);  // 배열 arr2의 길이 계산
	ar3Len = sizeof(arr3) / sizeof(int);  // 배열 arr3의 길이 계산

	for (i = 0; i < ar1Len; i++)  // arr1의 길이만큼 반복
		printf("%d ", arr1[i]);  // 저장된 값 출력
	printf("\n");

	for (i = 0; i < ar2Len; i++)  // arr2의 길이만큼 반복
		printf("%d ", arr2[i]);  // 저장된 값 출력
	printf("\n");

	for (i = 0; i < ar3Len; i++)  // arr3의 길이만큼 반복
		printf("%d ", arr3[i]);  // 저장된 값 출력
	printf("\n");
	return 0;
}